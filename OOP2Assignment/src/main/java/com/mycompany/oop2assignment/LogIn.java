        private boolean validateLogin(long nic, String password) {
        // Initialize DB connection
        DB db = new DB();
        Connection con = db.getCon();
        Statement stmt = null;
        ResultSet rs = null;
        boolean isValid = false;

        try {
            // Create SQL query to check if username and password exist in the user table
            String query = "SELECT * FROM user WHERE nic = '" + nic + "' AND password = '" + password + "'";
            stmt = con.createStatement();
            rs = stmt.executeQuery(query);

            // If a row is returned, username and password are correct
            isValid = rs.next();
        } catch (SQLException ex) {
            Logger.getLogger(LogIn.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            // Close resources
            try {
                if (rs != null) rs.close();
                if (stmt != null) stmt.close();
                if (con != null) con.close();
            } catch (SQLException ex) {
                Logger.getLogger(LogIn.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        return isValid;
    }
        
        
