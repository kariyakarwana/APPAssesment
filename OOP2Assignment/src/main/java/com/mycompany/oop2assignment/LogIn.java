    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nicString = jTextField1.getText().trim(); // Get the text from the JTextField
        char[] passwordChars = jPasswordField1.getPassword();
        String password = new String(passwordChars);
        if (nicString.isEmpty()) {
            JOptionPane.showMessageDialog(this, "NIC cannot be empty", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Exit the method if NIC is empty
        }

        try {
            long nic = Long.parseLong(nicString);

            if (validateLogin(nic, password)) {
                System.out.println("Login Successful"); // Debugging statement
                JOptionPane.showMessageDialog(this, "Login Successful");
                this.dispose();
                System.out.println("Opening Menu..."); // Debugging statement
                new Menu().setVisible(true); // Ensure Menu class is instantiated and displayed
            } else {
                System.out.println("Login Failed"); // Debugging statement
                JOptionPane.showMessageDialog(this, "Login Failed. Please check your NIC and password.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            // Handle the case where the input cannot be parsed as a long
            // For example, display an error message to the user
            JOptionPane.showMessageDialog(this, "Invalid NIC format. Please enter a valid number.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

