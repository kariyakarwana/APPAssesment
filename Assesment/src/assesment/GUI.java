    private void loadInitialSeatState() {
        for (int i = 0; i < checkBoxes.length; i++) {
            String seatNumber = Integer.toString(i + 1);
            if (!isSeatAvailable(seatNumber)) {
                // If seat is reserved, set checkbox selected and label text to "Reserved"
                checkBoxes[i].setSelected(true);
                seatLabels[i].setText("Reserved");
                // Disable checkbox so it cannot be unchecked
                checkBoxes[i].setEnabled(false);
            }
        }
    }
     private boolean isSeatAvailable(String seatNumber) {
        // Create an instance of ReservationHandler
        reservationHandler = new ReservationHandler("", seatNumber, obj, checkBoxes, seatLabels);
        // Call the isSeatAvailable method
        return reservationHandler.isSeatAvailable(seatNumber);
    }
    
