    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
       String userId = jTextField1.getText();

        ArrayList<String> selectedSeats = new ArrayList<>();
        if (jCheckBox1.isSelected()) selectedSeats.add("1");
        if (jCheckBox2.isSelected()) selectedSeats.add("2");
        if (jCheckBox3.isSelected()) selectedSeats.add("3");
        if (jCheckBox4.isSelected()) selectedSeats.add("4");
        if (jCheckBox5.isSelected()) selectedSeats.add("5");

        synchronized (this) {
            for (String seat : selectedSeats) {
                Thread thread = new Thread(new ReservationHandler(userId, seat, obj, checkBoxes, seatLabels));
                thread.start();
            }
        }

    }//GEN-LAST:event_jButton9ActionPerformed

